package basics;
/* 
 * Demonstrate For Loop
 */
public class ForDemo {

	public static void main(String[] args) {
		
		int i,j;
		
		System.out.println("********* Display 1-25 ************");
		
		for(i=1;i<=25;i++)
		{
			System.out.println(i);
		}
		
		System.out.println("********** Display 55 -20 **********");
		
		for(j=55;j>=20;j--)
		{
			System.out.println(j);
		}
		
	}

}
